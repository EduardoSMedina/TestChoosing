    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(String.valueOf(jComboBox1.getSelectedItem()).equals("Areas")){
           m.EliminarArea(String.valueOf(jComboBox2.getSelectedItem()));
           JOptionPane.showMessageDialog(null,jComboBox2.getSelectedItem()+" Eliminada");
           this.setVisible(false);
           Modificar mod = new Modificar();
           mod.setItem();
           mod.setVisible(true);
           
       }else{
           if(String.valueOf(jComboBox1.getSelectedItem()).equals("Preferencias")){
               m.EliminarPreferencia(String.valueOf(jComboBox2.getSelectedItem()));
               JOptionPane.showMessageDialog(null,jComboBox2.getSelectedItem()+" Eliminada");
               this.setVisible(false);
               Modificar mod = new Modificar();
               mod.setItem();
               mod.setVisible(true);
           }else{
               if(String.valueOf(jComboBox1.getSelectedItem()).equals("Carreras")){
                    m.EliminarCarrera(String.valueOf(jComboBox2.getSelectedItem()));
                    JOptionPane.showMessageDialog(null,jComboBox2.getSelectedItem()+" Eliminada");
                    this.setVisible(false);
                    Modificar mod = new Modificar();
                    mod.setItem();
                    mod.setVisible(true);
           }
        }
       }
    }//GEN-LAST:event_jButton2ActionPerformed

