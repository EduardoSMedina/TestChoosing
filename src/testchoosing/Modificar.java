    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
       if(String.valueOf(jComboBox1.getSelectedItem()).equals("Areas")){
           jComboBox2.removeAllItems();
           for (int i = 0; i < m.areas.size(); i++) {
               jComboBox2.addItem(m.areas.get(i).getNombre());
           }
       }else{
           if(String.valueOf(jComboBox1.getSelectedItem()).equals("Preferencias")){
               jComboBox2.removeAllItems();
               for (int i = 0; i < m.preferencias.size(); i++) {
               jComboBox2.addItem(m.preferencias.get(i).getNombre());
               }
           }else{
               if(String.valueOf(jComboBox1.getSelectedItem()).equals("Carreras")){
                  jComboBox2.removeAllItems(); 
                  for (int i = 0; i < m.carreras.size(); i++) {
                  jComboBox2.addItem(m.carreras.get(i).getNombre());
               }
           }
        }
       }
    }//GEN-LAST:event_jComboBox1ActionPerformed

